package divida.ativa.util.exception;

public class BOException extends Exception {

	private static final long serialVersionUID = 1176657722374581733L;

	public BOException() {
		super();
	}

	public BOException(String message, Throwable cause) {
		super(message, cause);
	}

	public BOException(String message) {
		super(message);
	}

	public BOException(Throwable cause) {
		super(cause);
	}

}
